#While loop
#Increasing values
number = 20
while number <= 25:
    print(number)
    number += 1

#decreasing values
count = 105
while count >= 100:
    print(count)
    count -= 1

#For loop
for x in range(1,5):
    print(x)

for num in range(20,30,2):
     print(num)

#List
language = ["Python", "Kotlin", "PHP"]
for lang in language:
    print(lang)